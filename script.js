function searchFaculty() {
    const keyword = document.getElementById('searchInput').value.toLowerCase();
            const resultsContainer = document.getElementById('results');
            resultsContainer.innerHTML = '';

            const matchedFaculty = facultyMembers.filter(member =>
                member.research.toLowerCase().includes(keyword)
            );

            if (matchedFaculty.length > 0) {
                matchedFaculty.forEach(member => {
                    const resultItem = document.createElement('div');
                    resultItem.innerHTML = `<a href="${member.link}" target="_blank">${member.name}</a>`;
                    resultsContainer.appendChild(resultItem);
                });
            } else {
                resultsContainer.innerHTML = '<p>No matching faculty member found.</p>';
            }
}

